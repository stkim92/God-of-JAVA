public class Calculator{
	public int sub(int _num1, int _num2){
		return _num1-_num2;
	}
	
	public int mul(int _num1, int _num2){
		return _num1*_num2;
	}
	
	public int div(int _num1, int _num2){
		return _num1/_num2;
	}
	
	
}